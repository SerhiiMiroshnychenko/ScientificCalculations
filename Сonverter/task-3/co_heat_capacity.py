# Імпортуємо необхідні бібліотеки
import numpy as np

# Константи
T_STANDARD = 273.15  # К (0°C)
P_STANDARD = 101325  # Па (1 атм)
R_CAL = 1.986       # кал/(моль·К)
R = 8.31446261815   # Дж/(моль·К)
CAL_TO_JOULE = 4.1868  # Дж/кал
M_CO = 0.028        # кг/моль (молярна маса CO)

# Коефіцієнти для CO з таблиці 1.1 (в кал/(моль·К))
a = 6.79           # константа
b = 0.98e-3        # коефіцієнт при T
c = -0.11e5        # коефіцієнт при T^(-2)

# Параметри задачі
t = 200            # °C
T = t + T_STANDARD # К

print(f"1. Вхідні дані:")
print(f"   Температура: {t}°C = {T:.2f} К")
print(f"   Тиск: {P_STANDARD/1000:.1f} кПа")
print(f"   Коефіцієнти для CO:")
print(f"   a = {a} кал/(моль·К)")
print(f"   b = {b:.3e} кал/(моль·К²)")
print(f"   c = {c:.3e} кал·К²/моль")

# Розрахунок мольної теплоємності при сталому тиску
cp_cal = a + b*T + c/(T**2)  # кал/(моль·К)
cp = cp_cal * CAL_TO_JOULE   # Дж/(моль·К)

print(f"\n2. Мольна теплоємність при сталому тиску:")
print(f"   cp = {cp_cal:.3f} кал/(моль·К) = {cp:.3f} Дж/(моль·К)")

# Розрахунок мольної теплоємності при сталому об'ємі
cv_cal = cp_cal - R_CAL      # кал/(моль·К)
cv = cv_cal * CAL_TO_JOULE   # Дж/(моль·К)

print(f"\n3. Мольна теплоємність при сталому об'ємі:")
print(f"   cv = {cv_cal:.3f} кал/(моль·К) = {cv:.3f} Дж/(моль·К)")

# Розрахунок питомої теплоємності
cv_mass = cv/M_CO  # Дж/(кг·К)

print(f"\n4. Питома теплоємність:")
print(f"   cv_пит = {cv_mass:.2f} Дж/(кг·К)")

# Розрахунок густини
rho = (P_STANDARD * M_CO)/(R * T)  # кг/м³

print(f"\n5. Густина CO при {t}°C:")
print(f"   ρ = {rho:.4f} кг/м³")

# Розрахунок об'ємної теплоємності
cv_vol = cv_mass * rho  # Дж/(м³·К)

print(f"\n6. Об'ємна теплоємність:")
print(f"   cv_об = {cv_vol:.2f} Дж/(м³·К)")

print(f"\n7. Перевірка розмірностей:")
print(f"   Питома теплоємність: [Дж/(моль·К)] / [кг/моль] = [Дж/(кг·К)] ✓")
print(f"   Об'ємна теплоємність: [Дж/(кг·К)] · [кг/м³] = [Дж/(м³·К)] ✓")
