% Константи
T_STANDARD = 273.15; % К (0°C)

% Коефіцієнти для теплоємності оксиду кальцію
a = 43.77; % кДж/(моль·К)
b = 4.525e-3; % кДж/(моль·К²)

% Вхідні дані
t1 = 1200; % °C
t2 = 1300; % °C

% Переведення температур в Кельвіни
T1 = t1 + T_STANDARD; % К
T2 = t2 + T_STANDARD; % К


% 1. Аналітичний метод
c_analytical = (a * (T2 - T1) + (b/2) * (T2^2 - T1^2)) / (T2 - T1);

fprintf('\n2. Середня питома теплоємність (аналітичний метод):\n');
fprintf('   c_сер = %.4f кДж/(моль·К)\n', c_analytical);

% 2. Метод трапецій
steps = 100000; % кількість кроків інтегрування
T = linspace(T1, T2, steps); % рівномірне розбиття інтервалу [T1, T2]
c_values = a + b*T; % теплоємність як функція від T
c_trapz = trapz(T, c_values) / (T2 - T1);

fprintf('\n3. Середня питома теплоємність (метод трапецій):\n');
fprintf('   c_сер = %.4f кДж/(моль·К)\n', c_trapz);

% 3. Метод Сімпсона
c_simpson = integral(@(T) a + b*T, T1, T2) / (T2 - T1);

fprintf('\n4. Середня питома теплоємність (метод Сімпсона):\n');
fprintf('   c_сер = %.4f кДж/(моль·К)\n', c_simpson);
