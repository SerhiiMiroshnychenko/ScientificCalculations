%{
% ОЦІНКА ФУНКЦІЇ НАЛЕЖНОСТІ У ЗАДАНИХ ТОЧКАХ
% ============================================
% Призначення: Демонстрація обчислення значень функції належності
% для конкретних точок (без графічної візуалізації)
%
% Функція evalmmf:
% Обчислює значення заданої функції належності у вказаних точках
%
% Синтаксис: y = evalmmf(x, params, 'function_type')
%   x - вектор точок, в яких обчислюється функція
%   params - параметри функції належності
%   'function_type' - тип функції (у даному випадку 'pimf')
%
% У цьому прикладі:
%   x = [3, 4, 5] - три точки для оцінки
%   params = [2 5 8 9] - параметри Pi-функції:
%       підйом: 2→5, плато: 5→8, спад: 8→9
%   'pimf' - Pi-подібна функція
%
% Очікувані результати:
%   x=3: знаходиться на підйомі (між 2 і 5), μ ≈ 0.28
%   x=4: ближче до плато (між 2 і 5), μ ≈ 0.84
%   x=5: початок плато, μ = 1.0
%
% Застосування: Перевірка належності конкретних значень до
% нечіткої множини, обчислення ступеня активації правил,
% класифікація даних
%}

% Вектор точок, для яких потрібно обчислити значення належності
x = [3 4 5];

% Обчислення значень Pi-функції у заданих точках
% Параметри [2 5 8 9]: підйом 2→5, плато 5→8, спад 8→9
% Результат виводиться у консоль
y = evalmmf (x, [2 5 8 9], 'pimf')