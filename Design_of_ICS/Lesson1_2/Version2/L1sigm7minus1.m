%{
% РІЗНИЦЯ ДВОХ СИГМОЇД - ВБУДОВАНА ФУНКЦІЯ (Difference Sigmoid)
% ==============================================================
% Призначення: Демонстрація dsigmf - різниці двох сигмоїдальних функцій
% Створює "горб" (bump function) між двома точками перегину
%
% Формула: μ(x) = sigmf(x,[a1,c1]) - sigmf(x,[a2,c2])
% де обидві сигмоїди зростаючі (a1>0, a2>0), але c1 < c2
%
% Параметри dsigmf: [a1, c1, a2, c2]
%   a1 - нахил лівої (першої) сигмоїди
%   c1 - ліва точка перегину (початок "горба")
%   a2 - нахил правої (другої) сигмоїди
%   c2 - права точка перегину (кінець "горба")
%
% У цьому прикладі: [5, 2, 5, 7]
%   - Ліва сигмоїда: крутизна 5, центр у x=2
%   - Права сигмоїда: крутизна 5, центр у x=7
%   - Результат: "горб" від x≈2 до x≈7
%
% Відмінність від L1sigm7minus2.m:
%   - Тут використовується готова функція dsigmf()
%   - У L1sigm7minus2.m - ручне обчислення різниці
%   - Результат ідентичний
%
% Застосування: Моделювання обмежених інтервалів, "приблизно між",
% виділення певного діапазону значень
%}

% Створення вектора значень x від 0 до 10 з кроком 0.2
x = (0:0.2:10);

% Обчислення різниці двох сигмоїд вбудованою функцією
% [5 2 5 7]: дві сигмоїди з однаковим нахилом 5, центри у 2 та 7
y=dsigmf(x,[5 2 5 7]);

% Побудова функції "горба"
plot(x,y);

% Додавання сітки для аналізу меж "горба"
grid;

% Підпис вертикальної осі (ступінь належності)
ylabel('y');

% Підпис горизонтальної осі (вхідний параметр)
xlabel('x')
