%{
% УЗАГАЛЬНЕНА ДЗВОНОПОДІБНА ФУНКЦІЯ - ВПЛИВ ПАРАМЕТРА ШИРИНИ
% ===========================================================
% Призначення: Демонстрація впливу параметра 'a' (ширина) на форму
% узагальненої дзвоноподібної функції належності (Generalized Bell MF)
%
% Формула: μ(x) = 1 / (1 + |((x-c)/a)|^(2b))
%
% Параметри gbellmf: [a, b, c]
%   a - визначає ширину дзвона (half-width на рівні μ=0.5)
%   b - визначає крутизну схилів (slope)
%   c - центр функції (точка максимуму)
%
% У цьому прикладі:
%   - Параметр 'a' змінюється (1, 2, 3) - показує зміну ширини
%   - Параметр 'b' = 1 (фіксований)
%   - Параметр 'c' = 5 (фіксований центр)
%
% Висновок: Збільшення 'a' призводить до розширення дзвона,
% тобто нечітка множина охоплює ширший діапазон значень
%
% Застосування: Налаштування чутливості нечітких правил
%}

% Створення вектора значень x від 0 до 10 з кроком 0.2
x = (0:0.2:10);

% Обчислення першої функції з вузьким дзвоном (a=1)
y1 = gbellmf(x, [1 1 5]);

% Обчислення другої функції із середньою шириною (a=2)
y2 = gbellmf(x, [2 1 5]);

% Обчислення третьої функції з широким дзвоном (a=3)
y3 = gbellmf(x, [3 1 5]);

% Побудова всіх трьох функцій на одному графіку для порівняння
plot(x,y1,x,y2,x,y3);

% Додавання сітки для аналізу ширини функцій
grid;

% Підпис вертикальної осі (ступінь належності)
ylabel('y');

% Підпис горизонтальної осі (вхідна змінна)
xlabel('x')