%{
% ДОБУТОК ДВОХ СИГМОЇД (Product of Two Sigmoids)
% ===============================================
% Призначення: Демонстрація psigmf - добутку двох сигмоїдальних функцій
% Результат - дзвоноподібна крива з можливістю асиметрії
%
% Формула: μ(x) = sigmf(x,[a1,c1]) × sigmf(x,[a2,c2])
% де перша сигмоїда зростає (a1>0), друга спадає (a2<0)
%
% Параметри psigmf: [[a1, c1], [a2, c2]]
%   [a1, c1] - параметри лівої (зростаючої) сигмоїди
%       a1 > 0 - позитивний нахил (підйом зліва)
%       c1 - ліва точка перегину
%   [a2, c2] - параметри правої (спадної) сигмоїди
%       a2 < 0 - від'ємний нахил (спад справа)
%       c2 - права точка перегину
%
% У цьому прикладі:
%   params1 = [2, 3]  - зростаюча сигмоїда з перегином у x=3
%   params2 = [-5, 8] - спадна сигмоїда з перегином у x=8
%
% Переваги:
%   - Комбінує дві сигмоїди в одну дзвоноподібну функцію
%   - Можливість асиметрії (різні нахили з обох боків)
%   - Гладкі переходи
%
% Застосування: Моделювання діапазонів "від-до", інтервальних
% нечітких множин типу "приблизно від 3 до 8"
%}

% Створення вектора значень x від 0 до 10 з кроком 0.2
x = (0:0.2:10);

% Параметри лівої (зростаючої) сигмоїди: нахил=2, центр=3
params1 = [2 3];

% Параметри правої (спадної) сигмоїди: нахил=-5, центр=8
params2 = [-5 8];

% Обчислення добутку двох сигмоїд (дзвоноподібна крива)
y = psigmf(x, [params1 params2]);

% Побудова результуючої функції належності
plot(x,y);

% Додавання сітки для аналізу форми "дзвона"
grid;

% Підпис вертикальної осі (ступінь належності)
ylabel('y');

% Підпис горизонтальної осі (значення змінної)
xlabel('x')