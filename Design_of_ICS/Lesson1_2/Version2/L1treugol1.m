%{
% ТРИКУТНА ФУНКЦІЯ НАЛЕЖНОСТІ (Triangular Membership Function)
% ============================================================
% Призначення: Демонстрація базової трикутної функції належності
% Функція trimf створює трикутну форму, яка використовується для 
% представлення нечітких множин типу "приблизно X"
% 
% Параметри trimf: [a b c]
%   a - ліва точка основи трикутника (де μ=0)
%   b - права точка основи трикутника (де μ=0)
%   c - вершина трикутника (де μ=1)
%
% Застосування: Моделювання лінгвістичних змінних ("низький", 
% "середній", "високий") у системах нечіткого керування
%}

% Створення вектора значень x від 0 до 10 з кроком 0.2
x = (0:0.2:10);

% Обчислення трикутної функції належності
% Параметри [3 7 5]: основа від 3 до 7, пік у точці 5
y = trimf(x, [3 7 5]);

% Побудова графіка функції належності
plot(x, y);

% Додавання сітки для кращої читабельності графіка
grid

% Підпис вертикальної осі (значення належності від 0 до 1)
ylabel('y')

% Підпис горизонтальної осі (універсальна множина)
xlabel('x')