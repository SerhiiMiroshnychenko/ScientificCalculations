%{
% ГАУСОВА ФУНКЦІЯ НАЛЕЖНОСТІ (Gaussian Membership Function)
% ==========================================================
% Призначення: Демонстрація гаусової (нормальної) функції належності
% Формула: μ(x) = exp(-(x-c)²/(2σ²))
% 
% Гаусова функція має дзвоноподібну форму з гладкими переходами
% та є однією з найпопулярніших функцій у нечіткій логіці
%
% Параметри gaussmf: [σ, c]
%   σ (sigma) - стандартне відхилення, визначає ширину кривої
%   c - центр розподілу (точка максимуму, де μ=1)
%
% Особливості:
%   - Гладка та диференційована на всій області
%   - Симетрична відносно центру
%   - Асимптотично наближається до 0
%
% Застосування: Моделювання "м'яких" переходів, нейронні мережі,
% системи розпізнавання образів
%}

% Створення вектора значень x від 0 до 10 з кроком 0.2
x = (0:0.2:10);

% Обчислення гаусової функції належності та побудова графіка
% Параметри [1 5]: σ=1 (ширина), c=5 (центр)
y = gaussmf(x, [1 5]); plot(x, y)

% Додавання сітки для аналізу форми функції
grid

% Підпис вертикальної осі (ступінь належності μ)
ylabel('y')

% Підпис горизонтальної осі (значення змінної)
xlabel('x')