%{
% ОЦІНКА ТА ПОРІВНЯННЯ КІЛЬКОХ ФУНКЦІЙ НАЛЕЖНОСТІ
% =================================================
% Призначення: Демонстрація одночасної оцінки та візуалізації
% декількох різнотипних функцій належності
%
% Функція evalmmf з об'єктами fismf:
% Дозволяє створити масив функцій належності та обчислити їх
% значення для всього діапазону універсальної множини
%
% Структура fismf:
%   fismf(@function, parameters) - створює об'єкт функції належності
%       @function - вказівник на функцію (gaussmf, trapmf, тощо)
%       parameters - параметри для цієї функції
%
% У цьому прикладі:
%   Функція 1: Гаусова (gaussmf)
%       Параметри [1.5, 5]: σ=1.5, центр=5
%   Функція 2: Трапецієподібна (trapmf)
%       Параметри [3 4 6 7]: підйом 3→4, плато 4→6, спад 6→7
%
% Діапазон оцінки: x ∈ [-2, 12] з кроком 0.1
%
% Результат: Графік з двома кривими для порівняння форм
%
% Застосування: Порівняльний аналіз різних функцій належності,
% налаштування параметрів для конкретної задачі, проектування
% систем нечіткого виведення
%}

% Створення масиву з двох різних функцій належності
% 1) Гаусова: σ=1.5, центр=5
% 2) Трапецієподібна: [3 4 6 7]
mf = [fismf(@gaussmf,[1.5 5]) fismf(@trapmf,[3 4 6 7])];

% Створення вектора-стовпця точок від -2 до 12 з кроком 0.1
% Апостроф (') транспонує вектор у стовпець
x = (-2:0.1:12)';

% Обчислення значень обох функцій для всіх точок x
% Результат: матриця, де кожен стовпець - значення однієї функції
y = evalmmf(mf,x);

% Побудова графіка з двома кривими (автоматично різні кольори)
plot(x,y); grid;

% Підпис горизонтальної осі (універсальна множина)
xlabel('Universe of discourse (x)'),ylabel('Membership value (y)')

% Легенда з описом параметрів кожної функції
legend('gaussmf, P=[1.5 5]','trapmf, P=[3 4 6 7]')