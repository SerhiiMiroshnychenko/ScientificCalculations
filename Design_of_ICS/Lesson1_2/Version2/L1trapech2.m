%{
% ТРАПЕЦІЄПОДІБНА ФУНКЦІЯ НАЛЕЖНОСТІ (Trapezoidal Membership Function)
% ======================================================================
% Призначення: Демонстрація трапецієподібної функції належності
% Функція trapmf має плоску верхню частину (плато), що означає 
% діапазон значень з повною належністю (μ=1)
%
% Параметри trapmf: [a b c d]
%   a - початок лівого схилу (де μ=0)
%   b - кінець лівого схилу (де μ=1, початок плато)
%   c - початок правого схилу (кінець плато, де μ=1)
%   d - кінець правого схилу (де μ=0)
%
% Застосування: Представлення діапазонів з "чіткою зоною", наприклад
% "нормальна температура" від 36.3°C до 36.9°C
%}

% Створення вектора значень x від 0 до 10 з кроком 0.2
x = (0:0.2:10);

% Обчислення трапецієподібної функції належності та побудова графіка
% Параметри [2 4 6 8]: підйом 2→4, плато 4→6, спуск 6→8
y = trapmf(x, [2 4 6 8]); plot(x, y)

% Додавання сітки для кращої читабельності графіка
grid

% Підпис вертикальної осі (ступінь належності)
ylabel('y')

% Підпис горизонтальної осі (вхідна змінна)
xlabel('x')