%{
% PI-ПОДІБНА ФУНКЦІЯ НАЛЕЖНОСТІ (Pi-shaped Membership Function)
% ==============================================================
% Призначення: Демонстрація pimf - Pi-подібної функції належності
% Комбінація S-функції та Z-функції для створення "горба"
%
% Формула:
%   μ(x) = smf(x, [a, b])  для x ≤ (b+c)/2  (зростання)
%   μ(x) = zmf(x, [c, d])  для x > (b+c)/2  (спад)
%
% Параметри pimf: [a, b, c, d]
%   a - початок лівого підйому (де μ=0)
%   b - кінець лівого підйому (де μ=1, початок плато)
%   c - початок правого спаду (кінець плато, де μ=1)
%   d - кінець правого спаду (де μ=0)
%
% У цьому прикладі: [2, 5, 8, 9]
%   - Підйом: від x=2 до x=5 (функція smf)
%   - Плато: від x=5 до x=8 (μ=1)
%   - Спад: від x=8 до x=9 (функція zmf)
%
% Структура:
%   x ≤ 2:      μ = 0
%   2 < x < 5:  μ плавно зростає (S-подібно)
%   5 ≤ x ≤ 8:  μ = 1 (плато)
%   8 < x < 9:  μ плавно спадає (Z-подібно)
%   x ≥ 9:      μ = 0
%
% Переваги:
%   - Гладкі переходи на обох кінцях
%   - Чітке плато повної належності
%   - Універсальна форма "горба" або "плато"
%
% Порівняння:
%   - Схожа на trapmf, але з гладкими (не лінійними) схилами
%   - Схожа на gbellmf, але з можливістю плато
%
% Застосування: Моделювання "нормального діапазону",
% "оптимальні значення", "приблизно від X до Y",
% діапазони з чіткими комфортними межами
%}

% Створення вектора значень x від 0 до 10 з кроком 0.2
x = (0:0.2:10);

% Обчислення Pi-подібної функції з плато
% [2 5 8 9]: підйом 2→5, плато 5→8, спад 8→9
y = pimf(x, [2 5 8 9]);

% Побудова графіка "горба" з плато
plot(x,y);

% Додавання сітки для аналізу структури функції
grid;

% Підпис вертикальної осі (ступінь належності)
ylabel('y');

% Підпис горизонтальної осі (параметр)
xlabel('x')