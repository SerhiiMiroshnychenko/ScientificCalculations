%{
% УЗАГАЛЬНЕНА ДЗВОНОПОДІБНА ФУНКЦІЯ - ВПЛИВ ПАРАМЕТРА КРУТИЗНИ
% =============================================================
% Призначення: Демонстрація впливу параметра 'b' (slope) на форму
% узагальненої дзвоноподібної функції належності
%
% Формула: μ(x) = 1 / (1 + |((x-c)/a)|^(2b))
%
% Параметри gbellmf: [a, b, c]
%   a - ширина дзвона
%   b - крутизна схилів (чим більше b, тим крутіші схили)
%   c - центр функції
%
% У цьому прикладі:
%   - Параметр 'a' = 1 (фіксована ширина)
%   - Параметр 'b' змінюється (1, 2, 3) - показує зміну крутизни
%   - Параметр 'c' = 5 (фіксований центр)
%
% Висновок: Збільшення 'b' робить схили більш крутими, функція
% стає більш подібною до трапецієподібної з чіткими переходами.
% При b→∞ функція наближається до прямокутної форми.
%
% Застосування: Моделювання "м'яких" (малі b) або "жорстких" 
% (великі b) меж між нечіткими множинами
%}

% Створення вектора значень x від 0 до 10 з кроком 0.2
x = (0:0.2:10);

% Обчислення функції з плавними схилами (b=1)
y1 = gbellmf(x, [1 1 5]);

% Обчислення функції з більш крутими схилами (b=2)
y2 = gbellmf(x, [1 2 5]);

% Обчислення функції з найкрутішими схилами (b=3)
y3 = gbellmf(x, [1 3 5]);

% Побудова всіх трьох кривих для порівняння крутизни
plot (x, y1, x, y2, x, y3);

% Додавання сітки для аналізу форми переходів
grid

% Підпис вертикальної осі (ступінь належності)
ylabel('y')

% Підпис горизонтальної осі (значення параметра)
xlabel('x')
