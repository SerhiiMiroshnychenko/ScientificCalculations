%{
% РІЗНИЦЯ ДВОХ СИГМОЇД - РУЧНЕ ОБЧИСЛЕННЯ
% ========================================
% Призначення: Демонстрація створення "горба" шляхом віднімання 
% двох сигмоїдальних функцій (альтернатива dsigmf)
%
% Формула: μ(x) = sigmf(x,[a1,c1]) - sigmf(x,[a2,c2])
%
% Математична ідея:
%   y1 = зростаюча сигмоїда (0→1), центр у x=2
%   y2 = зростаюча сигмоїда (0→1), центр у x=7
%   y3 = y1 - y2 = "горб" між двома центрами
%
% У цьому прикладі:
%   y1 = sigmf(x, [5, 2]) - рано досягає 1 (біля x=2)
%   y2 = sigmf(x, [5, 7]) - пізно досягає 1 (біля x=7)
%   y3 = y1 - y2 - різниця створює позитивний "горб"
%
% Логіка:
%   - При x < 2: y1≈0, y2≈0, різниця ≈0
%   - При 2 < x < 7: y1≈1, y2≈0-0.5, різниця позитивна (горб)
%   - При x > 7: y1≈1, y2≈1, різниця ≈0
%
% Порівняння з L1sigm7minus1.m:
%   - L1sigm7minus1.m використовує dsigmf() - вбудована функція
%   - Цей файл - ручне обчислення для розуміння механізму
%   - Результати ідентичні
%
% Застосування: Моделювання "вікна" значень, обмежених діапазонів
%}

% Створення вектора значень x від 0 до 10 з кроком 0.2
x = (0:0.2:10);

% Перша (ліва) зростаюча сигмоїда з центром у x=2
y1 = sigmf(x, [5 2]);

% Друга (права) зростаюча сигмоїда з центром у x=7
y2 = sigmf(x, [5 7]);

% Різниця двох сигмоїд - створює "горб"
y3 = y1-y2;

% Побудова результуючої функції (тільки різниці)
plot(x,y3);

% Додавання сітки для аналізу
grid;

% Підпис вертикальної осі (значення різниці)
ylabel('y');

% Підпис горизонтальної осі (вхідна змінна)
xlabel('x')
