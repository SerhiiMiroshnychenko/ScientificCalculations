%{
% СПАДНА СИГМОЇДАЛЬНА ФУНКЦІЯ (Decreasing Sigmoid)
% =================================================
% Призначення: Демонстрація сигмоїдальної функції з від'ємним нахилом
% При від'ємному параметрі 'a' функція спадає від 1 до 0
%
% Формула: μ(x) = 1 / (1 + exp(-a(x-c)))
% При a < 0: функція монотонно спадає (1 → 0)
%
% Параметри sigmf: [a, c]
%   a < 0 - від'ємний нахил (спадна функція)
%   c - точка перегину (де μ=0.5)
%
% У цьому прикладі:
%   - Параметр 'a' від'ємний: (-6, -4, -1)
%   - Точки перегину різні (c = 7, 5, 5)
%
% Особливості:
%   - При a = -6: дуже крутий спад біля x=7
%   - При a = -4: середня крутизна, спад біля x=5
%   - При a = -1: плавний спад біля x=5
%
% Застосування: Моделювання понять "менше ніж", "не більше",
% обернених залежностей (наприклад, "чим більше відстань, 
% тим менше привабливість")
%}

% Створення вектора значень x від 0 до 10 з кроком 0.2
x = (0:0.2:10);

% Обчислення спадної сигмоїди з крутим нахилом (a=-6, c=7)
y1 = sigmf(x, [-6 7]);

% Обчислення спадної сигмоїди із середнім нахилом (a=-4, c=5)
y2 = sigmf(x, [-4 5]);

% Обчислення спадної сигмоїди з плавним нахилом (a=-1, c=5)
y3 = sigmf(x, [-1 5]);

% Побудова всіх спадних кривих для порівняння
plot(x,y1,x,y2,x,y3)

% Додавання сітки для аналізу точок перегину
grid;

% Підпис вертикальної осі (ступінь належності)
ylabel('y');

% Підпис горизонтальної осі (параметр)
xlabel('x')