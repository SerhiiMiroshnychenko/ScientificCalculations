%{
% НЕЧІТКА АРИФМЕТИКА (Fuzzy Arithmetic Operations)
% =================================================
% Призначення: Демонстрація арифметичних операцій над нечіткими числами
% на основі принципу розширення Заде (Zadeh's Extension Principle)
%
% Теорія нечіткої арифметики:
% Для операції ○ (додавання, віднімання, множення, ділення):
%   μ_C(z) = sup{min(μ_A(x), μ_B(y)) | z = x ○ y}
%
% Вхідні нечіткі числа:
%   A - трапецієподібне число (діапазон приблизно від -10 до 3)
%   B - гаусове число (центр у 5, розкид σ=2)
%
% Операції:
%   1. A + B (додавання) - сума нечітких чисел
%   2. A - B (віднімання) - різниця нечітких чисел
%   3. A * B (множення) - добуток нечітких чисел
%   4. A / B (ділення) - частка нечітких чисел
%
% Результат: Кожна операція дає нову нечітку множину
%
% Візуалізація:
%   Figure 5: Додавання та віднімання
%   Figure 6: Множення та ділення
%
% Застосування: Обчислення з неточними даними, інженерні розрахунки
% з невизначеністю, фінансове моделювання, прогнозування
%}

% Кількість точок дискретизації (висока точність)
N = 501;

% Мінімальна межа універсальної множини
minX = -20;

% Максимальна межа універсальної множини
maxX = 20;

% Створення вектора дискретних точок
x = linspace(minX,maxX,N);

% Нечітке число A: трапецієподібна функція належності
% Параметри [-10 -2 1 3]: негативні та малі позитивні значення
A = trapmf(x,[-10 -2 1 3]);

% Нечітке число B: гаусова функція належності
% Параметри [2 5]: σ=2 (розкид), центр=5 (позитивне число)
B = gaussmf(x,[2 5]);

% === НЕЧІТКА АРИФМЕТИКА ===
% Обчислення суми нечітких чисел A і B (принцип розширення Заде)
Csum = fuzarith(x,A,B,'sum');

% Обчислення різниці нечітких чисел A і B
Csub = fuzarith(x,A,B,'sub');

% Обчислення добутку нечітких чисел A і B
Cprod = fuzarith(x,A,B,'prod');

% Обчислення частки нечітких чисел A і B
Cdiv = fuzarith(x,A,B,'div');

% === ВІЗУАЛІЗАЦІЯ: ДОДАВАННЯ ТА ВІДНІМАННЯ ===
% Створення п'ятого графічного вікна
figure(5)

% Верхній підграфік: додавання
subplot(2,1,1)
% Побудова: A (штрих), B (точки), A+B (бірюзова суцільна)
plot(x,A,'--',x,B,':',x,Csum,'c')
title('Fuzzy Addition, A+B')
legend('A','B','A+B')

% Нижній підграфік: віднімання
subplot(2,1,2)
% Побудова: A (штрих), B (точки), A-B (бірюзова суцільна)
plot(x,A,'--',x,B,':',x,Csub,'c')
title('Fuzzy Subtraction, A-B')
legend('A','B','A-B')

% === ВІЗУАЛІЗАЦІЯ: МНОЖЕННЯ ТА ДІЛЕННЯ ===
% Створення шостого графічного вікна
figure(6)

% Верхній підграфік: множення
subplot(2,1,1)
% Побудова: A (штрих), B (точки), A*B (бірюзова суцільна)
plot(x,A,'--',x,B,':',x,Cprod,'c')
title('Fuzzy Multiplication, A*B')
legend('A','B','A*B')

% Нижній підграфік: ділення
subplot(2,1,2)
% Побудова: A (штрих), B (точки), A/B (бірюзова суцільна)
plot(x,A,'--',x,B,':',x,Cdiv,'c')
title('Fuzzy Division, A/B')
legend('A','B','A/B')