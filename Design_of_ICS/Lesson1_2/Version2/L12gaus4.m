%{
% ПОДВІЙНА ГАУСОВА ФУНКЦІЯ НАЛЕЖНОСТІ (Two-sided Gaussian MF)
% ============================================================
% Призначення: Демонстрація асиметричної гаусової функції
% Функція gauss2mf комбінує дві гаусові функції для створення
% асиметричної дзвоноподібної кривої
%
% Формула:
%   μ(x) = exp(-(x-c1)²/(2σ1²))  для x ≤ c
%   μ(x) = exp(-(x-c2)²/(2σ2²))  для x > c
%   де c = (c1+c2)/2
%
% Параметри gauss2mf: [σ1, c1, σ2, c2]
%   σ1 - стандартне відхилення лівої частини
%   c1 - центр лівої гаусової функції
%   σ2 - стандартне відхилення правої частини
%   c2 - центр правої гаусової функції
%
% Переваги:
%   - Можливість моделювати асиметричні розподіли
%   - Гнучкість у налаштуванні лівого та правого схилів
%   - Гладкість переходів
%
% Застосування: Моделювання реальних процесів з асиметричними
% характеристиками (наприклад, час розігріву/охолодження)
%}

% Створення вектора значень x від 0 до 10 з кроком 0.2
x = (0:0.2:10);

% Обчислення подвійної гаусової функції та побудова графіка
% Параметри [1 5 1.5 6]: ліва (σ=1, c=5), права (σ=1.5, c=6)
% Ліва частина вужча (σ=1), права ширша (σ=1.5)
y = gauss2mf(x, [1 5 1.5 6]); plot(x, y);

% Додавання сітки для візуалізації асиметрії
grid

% Підпис вертикальної осі (ступінь належності)
ylabel('y')

% Підпис горизонтальної осі (простір ознак)
xlabel('x')