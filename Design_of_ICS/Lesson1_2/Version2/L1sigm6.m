%{
% СИГМОЇДАЛЬНА ФУНКЦІЯ НАЛЕЖНОСТІ (Sigmoid Membership Function)
% ==============================================================
% Призначення: Демонстрація впливу параметра нахилу на сигмоїдальну функцію
% Сигмоїда - S-подібна монотонна функція, популярна в нейронних мережах
%
% Формула: μ(x) = 1 / (1 + exp(-a(x-c)))
%
% Параметри sigmf: [a, c]
%   a - коефіцієнт нахилу (slope):
%       a > 0 - зростаюча функція (0→1)
%       a < 0 - спадна функція (1→0)
%       |a| більше - крутіший перехід
%   c - точка перегину (inflection point), де μ=0.5
%
% У цьому прикладі:
%   - Параметр 'a' змінюється (3, 2, 1) - різна крутизна
%   - Параметр 'c' = 5 (фіксована точка перегину)
%
% Висновок: Більше значення 'a' дає крутіший перехід від 0 до 1.
% При a→∞ функція наближається до ступінчастої (heaviside function).
%
% Застосування: Моделювання порогових процесів, активаційні функції,
% представлення понять "більше ніж", "принаймні"
%}

% Створення вектора значень x від 0 до 10 з кроком 0.2
x = (0:0.2:10);

% Обчислення сигмоїди з найкрутішим нахилом (a=3)
y1 = sigmf(x, [3 5]);

% Обчислення сигмоїди з найповільнішим нахилом (a=1)
y3 = sigmf(x, [1 5]);

% Обчислення сигмоїди із середнім нахилом (a=2)
y2 = sigmf(x, [2 5]);

% Побудова всіх трьох кривих для порівняння крутизни переходу
plot(x,y1,x,y2,x,y3)

% Додавання сітки для аналізу точки перегину
grid;

% Підпис вертикальної осі (ступінь належності 0-1)
ylabel('y');

% Підпис горизонтальної осі (вхідна змінна)
xlabel('x')