%{
% Z-ПОДІБНА ФУНКЦІЯ НАЛЕЖНОСТІ (Z-shaped Membership Function)
% ============================================================
% Призначення: Демонстрація zmf - Z-подібної функції належності
% Функція створює плавний перехід від 1 до 0 (спадна крива)
%
% Формула (кубічна сплайнова інтерполяція):
%   μ(x) = 1,                      якщо x ≤ a
%   μ(x) = 1 - 2((x-a)/(b-a))²,    якщо a ≤ x ≤ (a+b)/2
%   μ(x) = 2((x-b)/(b-a))²,        якщо (a+b)/2 ≤ x ≤ b
%   μ(x) = 0,                      якщо x ≥ b
%
% Параметри zmf: [a, b]
%   a - точка початку спаду (де μ=1→)
%   b - точка завершення спаду (де μ=0)
%   (a+b)/2 - точка перегину (де μ=0.5)
%
% У цьому прикладі: [2, 8]
%   - Спад починається в x=2
%   - Спад завершується в x=8
%   - Точка перегину в x=5
%
% Особливості:
%   - Дзеркальна протилежність до smf
%   - Гладкий спад (поліном 3-го степеня)
%   - Чіткі межі переходу
%
% Зв'язок з smf:
%   zmf(x,[a,b]) = 1 - smf(x,[a,b])
%
% Застосування: Моделювання "поступового зменшення",
% "менше або рівне", поняття "не більше ніж X",
% класифікація спадних характеристик
%}

% Створення вектора значень x від 0 до 10 з кроком 0.2
x = (0:0.2:10);

% Обчислення Z-подібної функції та побудова графіка
% Параметри [2 8]: спад від 1 до 0 в інтервалі x∈[2,8]
plot(x, zmf(x, [2 8]));

% Додавання сітки для аналізу точок спаду
grid;

% Підпис вертикальної осі (ступінь належності 1→0)
ylabel('y');

% Підпис горизонтальної осі (значення змінної)
xlabel('x')