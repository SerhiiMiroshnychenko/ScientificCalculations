%{
% S-ПОДІБНА ФУНКЦІЯ НАЛЕЖНОСТІ (S-shaped Membership Function)
% ============================================================
% Призначення: Демонстрація smf - S-подібної функції належності
% Функція створює плавний перехід від 0 до 1 (зростаюча крива)
%
% Формула (кубічна сплайнова інтерполяція):
%   μ(x) = 0,                      якщо x ≤ a
%   μ(x) = 2((x-a)/(b-a))²,        якщо a ≤ x ≤ (a+b)/2
%   μ(x) = 1 - 2((x-b)/(b-a))²,    якщо (a+b)/2 ≤ x ≤ b
%   μ(x) = 1,                      якщо x ≥ b
%
% Параметри smf: [a, b]
%   a - точка початку переходу (де μ=0)
%   b - точка завершення переходу (де μ=1)
%   (a+b)/2 - точка перегину (де μ=0.5)
%
% У цьому прикладі: [3, 8]
%   - Перехід починається в x=3
%   - Перехід завершується в x=8
%   - Точка перегину в x=5.5
%
% Особливості:
%   - Гладкіша за сигмоїду (використовує поліноми 3-го степеня)
%   - Симетрична S-подібна форма
%   - Фіксовані значення 0 і 1 за межами [a,b]
%
% Порівняння з sigmf:
%   - smf має чіткі межі [a,b]
%   - sigmf асимптотично наближається до 0 та 1
%
% Застосування: Моделювання "поступового зростання", 
% "більше або рівне", класифікація "від низького до високого"
%}

% Створення вектора значень x від 0 до 10 з кроком 0.2
x = (0:0.2:10);

% Обчислення S-подібної функції з переходом від 3 до 8
y=smf(x,[3 8]); 

% Побудова графіка зростаючої кривої
plot(x,y);

% Додавання сітки для візуалізації точок переходу
grid;

% Підпис вертикальної осі (ступінь належності 0→1)
ylabel('y');

% Підпис горизонтальної осі (вхідний параметр)
xlabel('x')
