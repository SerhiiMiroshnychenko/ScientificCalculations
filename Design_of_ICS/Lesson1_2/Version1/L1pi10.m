%{
% Скрипт демонструє побудову пі-подібної функції належності pimf, яка поєднує S- та Z-подібні сегменти.
% Мета – показати, як параметри початку та завершення кожного сегмента впливають на ширину ядра та плавність переходів.
% Кроки: створення універсуму дискурсу, обчислення pimf з вказаними параметрами та візуалізація результату з підписами осей.
%}
x = (0:0.2:10); % Формує вектор значень універсуму дискурсу від 0 до 10 з кроком 0.2
y = pimf(x, [2 5 8 9]); % Обчислює пі-подібну функцію належності з параметрами [a b c d]
plot(x,y); % Будує графік отриманої функції належності
grid; % Вмикає сітку для полегшення аналізу форми кривої
ylabel('y'); % Підписує вісь ординат як ступінь належності
xlabel('x') % Пояснює вісь абсцис як універсум дискурсу