%{
% Скрипт порівнює генералізовані дзвонові функції належності gbellmf зі спільним центром та різною шириною.
% Мета – показати, як параметр ширини впливає на розмах та форму нечіткого терма при фіксованій крутизні.
% Кроки: сформувати універсум дискурсу, обчислити три варіанти gbellmf із різними параметрами ширини та візуалізувати їх на одному графіку.
%}
x = (0:0.2:10); % Формує вектор значень універсуму дискурсу від 0 до 10 з кроком 0.2
y1 = gbellmf(x, [1 1 5]); % Обчислює дзвонову функцію з шириною 1, крутизною 1 та центром 5
y2 = gbellmf(x, [2 1 5]); % Обчислює дзвонову функцію з шириною 2 при незмінних крутизні та центрі
y3 = gbellmf(x, [3 1 5]); % Обчислює дзвонову функцію з шириною 3 для демонстрації ширшого ядра
plot(x,y1,x,y2,x,y3); % Будує три дзвонові функції на одному графіку для порівняння
grid; % Вмикає сітку для полегшення аналізу кривих
ylabel('y'); % Підписує вісь ординат як ступінь належності
xlabel('x') % Пояснює вісь абсцис як універсум дискурсу