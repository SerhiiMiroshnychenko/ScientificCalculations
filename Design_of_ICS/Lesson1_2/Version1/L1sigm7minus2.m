%{
% Скрипт демонструє різницю між двома сигмоїдальними функціями належності як спосіб формування вузької області належності.
% Мета – показати, як віднімання сигмоїд із різними крутизнами створює “вікно” високої належності.
% Кроки: сформувати універсум дискурсу, обчислити дві сигмоїди, знайти їх різницю та візуалізувати результат.
%}
x = (0:0.2:10); % Формує вектор значень універсуму дискурсу від 0 до 10 з кроком 0.2
y1 = sigmf(x, [5 2]); % Обчислює сигмоїдальну функцію з крутизною 5 та центром 2
y2 = sigmf(x, [5 7]); % Обчислює сигмоїдальну функцію з такою ж крутизною, але центром 7
y3 = y1 - y2; % Визначає різницю між сигмоїдами, формуючи обмежену область належності
plot(x,y3); % Будує графік отриманої функції належності
grid; % Вмикає сітку для полегшення аналізу кривої
ylabel('y'); % Підписує вісь ординат як ступінь належності
xlabel('x') % Пояснює вісь абсцис як універсум дискурсу
