%{
% Скрипт демонструє базові операції над нечіткими множинами, побудованими на гаусівських функціях належності.
% Мета – порівняти результати max, min, s-норми (probor) та t-норми (prod) для двох термів з різними центрами.
% Покроково: створення універсуму дискурсу, обчислення функцій належності, застосування операцій та візуалізація результатів на окремих графіках.
%}
N = 501; % Визначає кількість точок дискретизації універсуму дискурсу
minX = -5; % Задає мінімальне значення універсуму дискурсу
maxX = 20; % Задає максимальне значення універсуму дискурсу
x = linspace(minX,maxX,N); % Створює вектор рівномірно розподілених точок між minX та maxX
mf1 = gaussmf(x,[1 5]); % Обчислює першу гаусівську функцію належності з σ=1 і центром 5
mf2 = gaussmf(x,[1 7]); % Обчислює другу гаусівську функцію належності з σ=1 і центром 7
% обчислення max та побудова графіку результата обчислення
mf = max(mf1,mf2); % Виконує покомпонентний максимум як класичну операцію об’єднання нечітких множин
figure(1) % Відкриває перше вікно для графіка операції максимуму
plot(x,mf,'LineWidth',3); grid; % Будує графік максимуму та вмикає сітку для зручності аналізу
% обчислення min та побудова графіку результата обчислення
figure(2) % Відкриває друге вікно для графіка операції мінімуму
mf3 = min(mf1,mf2); % Виконує покомпонентний мінімум як операцію перетину для нечітких множин
plot(x,mf3,'LineWidth',3); grid; % Відображає результат мінімуму з товстою лінією та сіткою
% обчислення об'еднання та побудова графіку результата обчислення
figure(3) % Створює третє вікно для графіка s-норми
mf4 = probor([mf1; mf2]); % Обчислює дублікатну s-норму (probor) для двох функцій належності
plot(x,mf4,'LineWidth',3); grid; % Виводить графік s-норми та додає сітку
% обчислення перетин та побудова графіку результата обчислення
figure(4) % Відкриває четверте вікно для графіка t-норми
mf4 = prod([mf1; mf2]); % Обчислює алгебраїчний добуток як t-норму для перетину множин
plot(x,mf4,'LineWidth',3); grid; % Будує графік t-норми та вмикає сітку
