%{
% Скрипт порівнює генералізовані дзвонові функції gbellmf з різними параметрами крутизни при незмінній ширині та центрі.
% Мета – показати, як зміна крутизни впливає на кут нахилу “плечей” нечіткого терма.
% Кроки: сформувати універсум дискурсу, обчислити три функції з різними параметрами крутизни та візуалізувати їх одночасно.
%}
x = (0:0.2:10); % Формує вектор значень універсуму дискурсу від 0 до 10 з кроком 0.2
y1 = gbellmf(x, [1 1 5]); % Обчислює дзвонову функцію з крутизною 1, шириною 1 та центром 5
y2 = gbellmf(x, [1 2 5]); % Обчислює дзвонову функцію з більшою крутизною 2 при тих самих ширині та центрі
y3 = gbellmf(x, [1 3 5]); % Обчислює дзвонову функцію з крутизною 3 для демонстрації крутіших “плечей”
plot(x, y1, x, y2, x, y3); % Будує всі три функції на одному графіку для порівняння
grid % Вмикає сітку для полегшення аналізу кривих
ylabel('y') % Підписує вісь ординат як ступінь належності
xlabel('x') % Пояснює вісь абсцис як універсум дискурсу
