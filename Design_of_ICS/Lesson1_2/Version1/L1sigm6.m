%{
% Скрипт порівнює декілька сигмоїдальних функцій належності з однаковим центром, але різною крутизною.
% Мета – показати, як параметр нахилу впливає на різкість переходу нечіткого терма.
% Послідовність дій: створення універсуму дискурсу, обчислення трьох сигмоїд, побудова графіків для аналізу.
%}
x = (0:0.2:10); % Формує вектор значень універсуму дискурсу від 0 до 10 з кроком 0.2
y1 = sigmf(x, [3 5]); % Обчислює сигмоїдальну функцію з крутизною 3 та центром 5
y3 = sigmf(x, [1 5]); % Обчислює сигмоїдальну функцію з меншою крутизною 1 при тому ж центрі
y2 = sigmf(x, [2 5]); % Обчислює сигмоїдальну функцію з середньою крутизною 2 і центром 5
plot(x,y1,x,y2,x,y3) % Будує три сигмоїдальні криві на одному графіку для порівняння
grid; % Вмикає сітку для полегшення аналізу залежностей
ylabel('y'); % Підписує вісь ординат як ступінь належності
xlabel('x') % Пояснює вісь абсцис як універсум дискурсу