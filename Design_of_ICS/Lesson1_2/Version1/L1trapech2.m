%{
% Скрипт демонструє побудову трапецеїдальної функції належності trapmf, яка має плато з повною належністю.
% Мета – показати, як параметри початку та завершення плато, а також “плечей” впливають на форму функції.
% Покроково: сформувати універсум дискурсу, обчислити trapmf із заданими параметрами та відобразити графік з пояснювальними підписами.
%}
x = (0:0.2:10); % Формує вектор значень універсуму дискурсу від 0 до 10 з кроком 0.2
y = trapmf(x, [2 4 6 8]); plot(x, y) % Обчислює трапецеїдальну функцію з параметрами [a b c d] та одразу будує графік
grid % Вмикає сітку для полегшення аналізу форми кривої
ylabel('y') % Підписує вісь ординат як ступінь належності
xlabel('x') % Пояснює вісь абсцис як універсум дискурсу