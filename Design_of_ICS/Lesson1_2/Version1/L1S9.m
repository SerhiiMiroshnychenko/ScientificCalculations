%{
% Скрипт демонструє побудову S-подібної функції належності, яка описує плавне зростання ступеня належності.
% Мета – проілюструвати вплив параметрів початку й завершення переходу на форму S-функції.
% Кроки: створення універсуму дискурсу, обчислення smf та візуалізація результату з пояснювальними підписами.
%}
x = (0:0.2:10); % Формує вектор значень універсуму дискурсу від 0 до 10 з кроком 0.2
y = smf(x,[3 8]); % Обчислює S-подібну функцію належності з параметрами переходу 3 та 8
plot(x,y); % Будує графік отриманої функції належності
grid; % Вмикає сітку для полегшення аналізу кривої
ylabel('y'); % Підписує вісь ординат як значення ступеня належності
xlabel('x') % Пояснює вісь абсцис як універсум дискурсу
