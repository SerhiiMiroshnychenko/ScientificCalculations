%{
% Скрипт демонструє нечітку арифметику між двома функціями належності, представленими трапецеїдальною та гаусівською формами.
% Мета – обчислити суму, різницю, добуток та ділення двох нечітких чисел і проілюструвати вплив операцій на форму результату.
% Послідовність: формування універсуму дискурсу, визначення вхідних множин, виконання операцій fuzarith та побудова графіків для кожної операції.
%}

N = 501; % Кількість точок дискретизації універсуму дискурсу
minX = -20; % Мінімальна границя універсуму дискурсу
maxX = 20; % Максимальна границя універсуму дискурсу
x = linspace(minX,maxX,N); % Створення рівномірного вектора значень між minX та maxX
A = trapmf(x,[-10 -2 1 3]); % Обчислення трапецеїдальної функції належності з заданими параметрами
B = gaussmf(x,[2 5]); % Обчислення гаусівської функції належності з σ=2 і центром 5

% Обчислення нечіткої суми, різниці, добутку та ділення множин A і B
Csum = fuzarith(x,A,B,'sum'); % Обчислення нечіткої суми множин A і B
Csub = fuzarith(x,A,B,'sub'); % Обчислення нечіткої різниці A і B
Cprod = fuzarith(x,A,B,'prod'); % Обчислення нечіткого добутку A і B
Cdiv = fuzarith(x,A,B,'div'); % Обчислення нечіткого ділення A на B

% Побудова графіків для операцій додавання та віднімання
figure(5) % Підготовка вікна для відображення результатів додавання та віднімання
subplot(2,1,1) % Розміщення графіка додавання у верхній частині фігури
plot(x,A,'--',x,B,':',x,Csum,'c') % Побудова функцій A, B та їх нечіткої суми
title('Fuzzy Addition, A+B') % Заголовок підграфіка для додавання
legend('A','B','A+B') % Легенда з позначенням вихідних множин і результату
subplot(2,1,2) % Розміщення графіка віднімання у нижній частині фігури
plot(x,A,'--',x,B,':',x,Csub,'c') % Побудова функцій A, B та їх нечіткої різниці
title('Fuzzy Subtraction, A-B') % Заголовок підграфіка для віднімання
legend('A','B','A-B') % Легенда для порівняння вхідних множин і результату різниці
figure(6) % Відкриття нового вікна для результатів добутку та ділення
subplot(2,1,1) % Розміщення графіка добутку у верхній області фігури
plot(x,A,'--',x,B,':',x,Cprod,'c') % Побудова функцій A, B та їх нечіткого добутку
title('Нечітке множення, A*B') % Заголовок підграфіка для добутку
legend('A','B','A*B') % Легенда з позначенням множин та їх добутку
subplot(2,1,2) % Розміщення графіка ділення у нижній області фігури
plot(x,A,'--',x,B,':',x,Cdiv,'c') % Побудова функцій A, B та їх нечіткого ділення
title('Нечітке ділення, A/B') % Заголовок підграфіка для ділення
legend('A','B','A/B') % Легенда з позначенням множин і результату ділення