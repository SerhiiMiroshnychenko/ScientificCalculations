%{
% Скрипт демонструє побудову трикутної функції належності trimf для нечіткого терма з асиметричною формою.
% Мета – показати, як зміна параметрів вершин впливає на форму кривої належності.
% Кроки: створити універсум дискурсу, обчислити значення trimf та відобразити результат із підписами осей.
%}
x = (0:0.2:10); % Формує вектор значень універсуму дискурсу від 0 до 10 з кроком 0.2
y = trimf(x, [3 7 5]); % Обчислює трикутну функцію належності з вершинами у точках 3, 7 та 5
plot(x, y); % Будує графік отриманої трикутної функції належності
grid % Вмикає сітку для полегшення аналізу форми кривої
ylabel('y') % Підписує вісь ординат як ступінь належності
xlabel('x') % Пояснює вісь абсцис як універсум дискурсу