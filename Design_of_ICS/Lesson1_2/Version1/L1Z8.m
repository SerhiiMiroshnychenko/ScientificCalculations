%{
% Скрипт демонструє побудову Z-подібної функції належності, яка описує плавне спадання належності.
% Мета – показати, як параметри початку та кінця переходу впливають на форму Z-функції.
% Покроково: створюється універсум дискурсу, обчислюється zmf та візуалізується результат з пояснювальними підписами.
%}
x = (0:0.2:10); % Формує вектор значень універсуму дискурсу від 0 до 10 з кроком 0.2
plot(x, zmf(x, [2 8])); % Обчислює Z-подібну функцію належності з параметрами переходу 2 і 8 та будує графік
grid; % Вмикає сітку для кращого візуального аналізу поведінки функції
ylabel('y'); % Підписує вісь ординат як значення ступеня належності
xlabel('x') % Пояснює вісь абсцис як універсум дискурсу