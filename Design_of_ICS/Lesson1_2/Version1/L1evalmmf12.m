%{
% Скрипт демонструє оцінювання набору функцій належності за допомогою evalmmf для порівняння різних типів термів.
% Мета – показати, як gaussmf і trapmf поводяться на спільному універсумі та як їх значення можна відобразити однією командою plot.
% Кроки: сформувати масив функцій належності, підготувати універсум дискурсу, обчислити значення evalmmf та побудувати графік з легендою.
%}
mf = [fismf(@gaussmf,[1.5 5]) fismf(@trapmf,[3 4 6 7])]; % Створює масив об’єктів функцій належності: гаусівської та трапецеїдальної
x = (-2:0.1:12)'; % Формує вектор-стовпець універсуму дискурсу від -2 до 12 з кроком 0.1
y = evalmmf(mf,x); % Обчислює значення обох функцій належності для кожної точки універсуму
plot(x,y); grid; % Будує графік отриманих значень та вмикає сітку для зручності аналізу
xlabel('Universe of discourse (x)'),ylabel('Membership value (y)') % Додає підписи осей: універсум та ступінь належності
legend('gaussmf, P=[1.5 5]','trapmf, P=[3 4 6 7]') % Виводить легенду з позначенням кожної функції належності