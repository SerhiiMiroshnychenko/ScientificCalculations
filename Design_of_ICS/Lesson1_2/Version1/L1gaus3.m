%{
% Скрипт демонструє побудову гаусівської функції належності для одного нечіткого терма.
% Мета прикладу – показати вплив параметрів ширини та центру на форму кривої належності.
% Код послідовно створює універсум дискурсу, обчислює значення функції належності та візуалізує результат.
%}
x = (0:0.2:10); % Формує рівномірний вектор значень універсуму дискурсу від 0 до 10 з кроком 0.2
y = gaussmf(x, [1 5]); plot(x, y) % Обчислює гаусівську функцію належності з параметрами σ=1 і центром 5 та будує графік
grid % Увімкнення сітки для полегшення візуального аналізу графіка
ylabel('y') % Підпис осі ординат, що відображає значення функції належності
xlabel('x') % Підпис осі абсцис, який задає універсум дискурсу