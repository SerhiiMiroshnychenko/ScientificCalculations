%{
% Скрипт демонструє побудову комбінованої сигмоїдальної функції psigmf як добутку зростаючої та спадної сигмоїд.
% Мета – показати, як зміна параметрів двох сигмоїд формує “дзвонову” форму нечіткого терма.
% Кроки: сформувати універсум дискурсу, задати параметри сигмоїд, обчислити комбіновану функцію та візуалізувати її.
%}
x = (0:0.2:10); % Формує вектор значень універсуму дискурсу від 0 до 10 з кроком 0.2
params1 = [2 3]; % Набір параметрів для першої (зростаючої) сигмоїдальної функції
params2 = [-5 8]; % Набір параметрів для другої (спадної) сигмоїдальної функції
y = psigmf(x, [params1 params2]); % Обчислює комбіновану сигмоїдальну функцію на основі двох наборів параметрів
plot(x,y); % Будує графік отриманої комбінованої функції належності
grid; % Вмикає сітку для полегшення аналізу кривої
ylabel('y'); % Підписує вісь ординат як значення ступеня належності
xlabel('x') % Пояснює вісь абсцис як універсум дискурсу