% Практична робота №4 — Варіант 20. Видаткова частина теплового балансу.

% =========================================================================
% Вхідні дані Варіанта 20 (з ПР №1, 3)
% =========================================================================
variant_no = 20;
m_steel = 920.60;  % кг
m_slag  = 142.63;  % кг
m_CO    = 66.91;   % кг
m_CO2   = 11.68;   % кг
m_dust  = 15.0;    % кг
Q_in    = 1721.48; % МДж

% =========================================================================
% Варіантні параметри ПР №4
% =========================================================================
t_tap  = 1616.0;  % °C — цільова температура випуску (Варіант 20)
k_loss = 0.050;   % 5.00% — коефіцієнт теплових втрат (Варіант 20)

% =========================================================================
% 1) Крок 1: Ентальпія рідкої сталі
% =========================================================================
H_ST_REF = 1250.0; % кДж/кг
CP_ST    = 0.836;  % кДж/(кг·К)
T_M      = 1535.0; % °C
h_steel  = H_ST_REF + CP_ST * (t_tap - T_M); % кДж/кг
Q_steel  = m_steel * h_steel / 1000.0;       % МДж

% =========================================================================
% 2) Крок 2: Ентальпія шлаку
% =========================================================================
H_SL_REF = 209.0;  % кДж/кг
CP_SL    = 0.90;   % кДж/(кг·К)
t_slag   = t_tap + 50.0;
h_slag   = H_SL_REF + CP_SL * t_slag;        % кДж/кг
Q_slag   = m_slag * h_slag / 1000.0;         % МДж

% =========================================================================
% 3) Крок 3: Тепло відхідних газів
% =========================================================================
t_gas    = 1500.0;
cp_CO    = 1.17;   % кДж/(кг·К)
cp_CO2   = 1.15;   % кДж/(кг·К)
Q_gas    = (m_CO * cp_CO + m_CO2 * cp_CO2) * t_gas / 1000.0; % МДж

% =========================================================================
% 4) Крок 4: Ентальпія пилу
% =========================================================================
t_dust   = 1500.0;
cp_dust  = 0.82;   % кДж/(кг·К)
Q_dust   = m_dust * cp_dust * t_dust / 1000.0; % МДж

% =========================================================================
% 5) Крок 5: Теплові втрати (футерівка + випромінювання)
% =========================================================================
Q_loss   = k_loss * Q_in;                      % МДж

% =========================================================================
% 6) Крок 6: Сумарний видаток та тепловий надлишок
% =========================================================================
Q_out    = Q_steel + Q_slag + Q_gas + Q_dust + Q_loss;
delta_Q  = Q_in - Q_out;
share_steel_pct = Q_steel / Q_out * 100.0;

% =========================================================================
% 7) Вивід результатів
% =========================================================================
separator = repmat('=', 1, 65);
fprintf('%s\n', separator);
fprintf('  ПРАКТИЧНА РОБОТА №4: ВИДАТОК ТЕПЛА (ВАРІАНТ №%d)\n', variant_no);
fprintf('%s\n', separator);
fprintf('  1) Ентальпія сталі   h = %.2f кДж/кг  ->  %.2f МДж\n', h_steel, Q_steel);
fprintf('  2) Ентальпія шлаку   h = %.2f кДж/кг  ->  %.2f МДж\n', h_slag, Q_slag);
fprintf('  3) Тепло газів       (t_gas = %.0f °C)       %.2f МДж\n', t_gas, Q_gas);
fprintf('  4) Тепло пилу        (t_dust = %.0f °C)      %.2f МДж\n', t_dust, Q_dust);
fprintf('  5) Теплові втрати    (k = %.3f = %.2f%%)     %.2f МДж\n', k_loss, k_loss*100, Q_loss);
fprintf('%s\n', repmat('-', 1, 65));
fprintf('  СУМАРНИЙ ВИДАТОК  Q_out  =  %.2f МДж\n', Q_out);
fprintf('  ЗАГАЛЬНИЙ ПРИХІД  Q_in   =  %.2f МДж\n', Q_in);
fprintf('  ТЕПЛОВИЙ НАДЛИШОК dQ     =  %+0.2f МДж\n', delta_Q);
fprintf('%s\n', separator);

fprintf('\n  Частка сталі у видатку: %.1f%%  ', share_steel_pct);

if share_steel_pct >= 70.0 && share_steel_pct <= 80.0
    fprintf('[OK: 70–80%%]\n');
else
    fprintf('[УВАГА: поза діапазоном 70–80%% — перевірте формули]\n');
end

fprintf('\n');

if delta_Q > 0
    fprintf('  РЕЗУЛЬТАТ: Надлишок %.2f МДж передається до ПР №5.\n', delta_Q);
    fprintf('  Розрахунок маси охолоджувача (брухту/руди) — наступний крок.\n');
elseif delta_Q == 0
    fprintf('  РЕЗУЛЬТАТ: Баланс точно замкнений. dQ = 0.\n');
else
    fprintf('  УВАГА: Тепловий дефіцит! Перевірте вхідні дані та формули.\n');
end

fprintf('%s\n', separator);
