%% Практична робота №2 (MATLAB)
% Кисневий баланс плавки та розрахунок режиму продувки (статична модель)

clc;
clear;

% =========================================================
% ВХІДНІ ДАНІ (ВАРІАНТ 20 — демонстраційний приклад)
% =========================================================

variant_no = 20;

% База розрахунку
m_charge = 1000.0;  % кг

% Дані з Практичної роботи №1
dSi = 10.504;
dMn = 5.179;
dP  = 0.202;
dC  = 31.879;
m_Fe_to_slag = 16.631;

% Дані варіанта для Практичної №2
PCR = 0.150;   % частка повного окиснення C до CO2
i_sp = 4.00;   % Нм3/(т*хв)

% Типові параметри
eta_O2 = 0.95;
phi_O2 = 0.995;

% =========================================================
% КОНСТАНТИ — СТЕХІОМЕТРИЧНІ КОЕФІЦІЄНТИ (Нм3/кг)
% =========================================================

q_v_Si = 0.798;
q_v_Mn = 0.204;
q_v_P  = 0.905;
q_v_Fe = 0.201;

q_v_C_CO  = 0.933;
q_v_C_CO2 = 1.866;

% =========================================================
% РОЗРАХУНОК
% =========================================================

% 1) Кисень на Si, Mn, P, Fe
V_Si = dSi * q_v_Si;
V_Mn = dMn * q_v_Mn;
V_P  = dP  * q_v_P;
V_Fe = m_Fe_to_slag * q_v_Fe;

% 2) Кисень на вуглець з урахуванням PCR
q_v_C_avg = q_v_C_CO * (1.0 - PCR) + q_v_C_CO2 * PCR;
V_C = dC * q_v_C_avg;

% 3) Теоретичний об’єм
V_O2_theor = V_Si + V_Mn + V_P + V_Fe + V_C;

% 4) Фактичний об’єм
V_O2_actual = V_O2_theor / (eta_O2 * phi_O2);

% 5) Параметри продувки
Q_O2 = i_sp * (m_charge / 1000.0);  % Нм3/хв
blow_time = V_O2_actual / Q_O2;     % хв

% =========================================================
% ВИВІД
% =========================================================

fprintf('%s\n', repmat('=',1,72));
fprintf('Практична робота №2 (MATLAB). Варіант %d\n', variant_no);
fprintf('%s\n', repmat('=',1,72));

fprintf('1) Складові теоретичної потреби в кисні (Нм3):\n');
fprintf('   V(Si) = %.3f\n', V_Si);
fprintf('   V(Mn) = %.3f\n', V_Mn);
fprintf('   V(P)  = %.3f\n', V_P);
fprintf('   V(Fe) = %.3f\n', V_Fe);
fprintf('   q_v(C,avg) = %.5f Нм3/кг\n', q_v_C_avg);
fprintf('   V(C)  = %.3f\n', V_C);

fprintf('%s\n', repmat('-',1,72));

fprintf('2) Підсумок:\n');
fprintf('   V_theor   = %.3f Нм3\n', V_O2_theor);
fprintf('   V_actual  = %.3f Нм3\n', V_O2_actual);

fprintf('%s\n', repmat('-',1,72));

fprintf('3) Режим продувки:\n');
fprintf('   Q_O2      = %.2f Нм3/хв\n', Q_O2);
fprintf('   blow_time = %.2f хв\n', blow_time);

fprintf('%s\n', repmat('=',1,72));
